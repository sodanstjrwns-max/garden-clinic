export function getAllFaqs(): { category: string; q: string; a: string }[] {
  return FAQ_CATEGORIES.flatMap((c) => c.items.map((i) => ({ category: c.label, q: i.q, a: i.a })))
}

export function getFaqCategory(slug: string): FaqCategory | undefined {
  return FAQ_CATEGORIES.find((c) => c.slug === slug)
}
